export const getWebsite = (storeId: number, game: 'riftbound' | 'lorcana'): string | null => {
  const store = stores.find(s => s.id === storeId);
  if (!store) return null;

  if (game === 'riftbound' && store.riftboundWebsite) {
    return store.riftboundWebsite;
  } else if (game === 'lorcana' && store.lorcanaWebsite) {
    return store.lorcanaWebsite;
  } else if (store.website) {
    return store.website;
  }

  return null;
}